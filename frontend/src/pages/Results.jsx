import React, { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import { Award, ShieldAlert, Sparkles } from 'lucide-react';
import { predictAPI } from '../services/api';
import CareerCard from '../components/CareerCard';
import { RadarChart } from '../components/Charts';

export default function Results() {
  const navigate = useNavigate();
  const [recommendations, setRecommendations] = useState([]);
  const [savedCareers, setSavedCareers] = useState([]);
  const [loading, setLoading] = useState(true);
  const [successMsg, setSuccessMsg] = useState('');

  const loadResults = async () => {
    try {
      setLoading(true);
      const res = await predictAPI.recommend();
      setRecommendations(res.data || []);
      
      const savedRes = await predictAPI.getSaved();
      setSavedCareers(savedRes.data.map(c => c.career_title) || []);
    } catch (err) {
      console.error(err);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    loadResults();
  }, []);

  const handleSave = async (careerTitle, matchPercentage) => {
    try {
      await predictAPI.saveCareer(careerTitle, matchPercentage);
      setSavedCareers(prev => [...prev, careerTitle]);
      setSuccessMsg(`Success! Career '${careerTitle}' bookmarked.`);
      setTimeout(() => setSuccessMsg(''), 4000);
    } catch (err) {
      console.error(err);
    }
  };

  if (loading) {
    return (
      <div className="h-[70vh] flex items-center justify-center">
        <div className="w-10 h-10 border-4 border-primary-500 border-t-transparent rounded-full animate-spin" />
      </div>
    );
  }

  // Formatting radar metrics based on top recommendation match
  const bestMatch = recommendations[0] || { match_percentage: 75 };
  const radarData = [
    { subject: 'Technical Match', A: bestMatch.match_percentage, fullMark: 100 },
    { subject: 'Interests Fit', A: Math.max(30, bestMatch.match_percentage - 5), fullMark: 100 },
    { subject: 'Values Match', A: 82, fullMark: 100 },
    { subject: 'Growth Potential', A: bestMatch.growth_rate ? parseInt(bestMatch.growth_rate) * 3.5 : 70, fullMark: 100 },
    { subject: 'Personality Fit', A: 78, fullMark: 100 }
  ];

  return (
    <div className="space-y-8 max-w-7xl mx-auto font-sans">
      <div className="flex items-center gap-3">
        <div className="w-10 h-10 rounded-xl bg-primary-600 flex items-center justify-center text-white">
          <Award className="w-5 h-5" />
        </div>
        <div>
          <h2 className="text-xl font-bold">AI Career Results</h2>
          <p className="text-xs text-slate-400">Discover your top recommended roles generated by our ML models</p>
        </div>
      </div>

      {successMsg && (
        <div className="p-4 bg-accent-emerald/10 border border-accent-emerald/20 text-accent-emerald text-xs font-medium rounded-xl">
          {successMsg}
        </div>
      )}

      <div className="grid grid-cols-1 lg:grid-cols-3 gap-6 items-start">
        {/* Recommendations list */}
        <div className="lg:col-span-2 space-y-6">
          <h3 className="text-sm font-bold text-slate-700 dark:text-slate-350 uppercase tracking-wider">Top matching roles</h3>
          {recommendations.length === 0 ? (
            <div className="glass-card p-10 text-center rounded-3xl">
              <p className="text-xs text-slate-500">Run the assessment form to calculate results.</p>
            </div>
          ) : (
            <div className="grid grid-cols-1 gap-6">
              {recommendations.map((career) => (
                <CareerCard 
                  key={career.career} 
                  career={career}
                  isSaved={savedCareers.includes(career.career)}
                  onSave={handleSave}
                  onViewRoadmap={() => navigate('/roadmap')}
                />
              ))}
            </div>
          )}
        </div>

        {/* Charts summary panels */}
        <div className="space-y-6">
          <h3 className="text-sm font-bold text-slate-700 dark:text-slate-350 uppercase tracking-wider">Fit Analysis</h3>
          <RadarChart data={radarData} title="Match breakdown (Highest Fit)" />

          <div className="glass-card p-6 rounded-2xl border border-slate-100 dark:border-dark-800 space-y-3">
            <h4 className="font-bold text-xs">How we calculate fits:</h4>
            <p className="text-[11px] text-slate-500 dark:text-slate-400 leading-relaxed">
              Our machine learning pipeline uses a TF-IDF Vectorizer to format your assessments' self-ratings, technical correct inputs, and specified interests, predicting matching labels from a Random Forest Classifier.
            </p>
            <div className="flex gap-2 p-3 bg-primary-500/5 rounded-xl border border-primary-500/10 text-[10px] text-primary-500 leading-normal">
              <Sparkles className="w-4 h-4 shrink-0" />
              <span>Verify that you complete the Resume upload to update ATS scoring metrics.</span>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
